class FoodModel {
  String foodName;
  double grams;
  int portions;
  double kcal;
  double carbs;
  double sugars;
  double fats;
  double proteins;

  FoodModel({
    required this.foodName,
    required this.grams,
    required this.portions,
    required this.kcal,
    required this.carbs,
    required this.sugars,
    required this.fats,
    required this.proteins
  });
}